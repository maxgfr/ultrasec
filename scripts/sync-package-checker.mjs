#!/usr/bin/env node
// Vendor package-checker.sh (https://github.com/maxgfr/package-checker.sh) into
// src/vendor/, pinned to a release tag. It is a single self-contained bash
// script (no runtime deps beyond bash/awk/curl) — rather than shipping it as a
// sibling file (which would silently vanish from any packaging path that only
// copies scripts/ultrasec.mjs), this GENERATES a deterministic TS module that
// embeds the script text as a string constant. tsup then inlines that module
// into the single-file bundle like any other source import, and the adapter
// materializes it to the cache dir at runtime (src/tools/package-checker.ts).
//
//   node scripts/sync-package-checker.mjs --ref v1.11.4   # fetch + pin + generate
//   node scripts/sync-package-checker.mjs --check         # offline drift/tamper gate (CI)
//
// The fetched .sh bytes are written UNMODIFIED (byte-identical to upstream) —
// package-checker.meta.json carries the pin ({tag, scriptVersion, sha256, syncedAt}),
// and --check re-hashes the vendored .sh AND re-renders the .ts from it, asserting
// byte-identity against what's committed (tamper of either file fails).
import { createHash } from "node:crypto";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const vendorDir = join(root, "src", "vendor");
const shPath = join(vendorDir, "package-checker.sh");
const tsPath = join(vendorDir, "package-checker-script.ts");
const metaPath = join(vendorDir, "package-checker.meta.json");

const sha256 = (buf) => createHash("sha256").update(buf).digest("hex");

/** Deterministic TS module from the .sh bytes — same input ⇒ byte-identical output. */
function renderTs(tag, shBuf) {
  const hash = sha256(shBuf);
  const text = shBuf.toString("utf8");
  return (
    "// GENERATED by scripts/sync-package-checker.mjs — DO NOT EDIT.\n" +
    `export const PACKAGE_CHECKER_TAG = ${JSON.stringify(tag)};\n` +
    `export const PACKAGE_CHECKER_SHA256 = ${JSON.stringify(hash)};\n` +
    `export const PACKAGE_CHECKER_SH = ${JSON.stringify(text)};\n`
  );
}

const args = process.argv.slice(2);
if (args[0] === "--check") {
  let meta;
  try {
    meta = JSON.parse(readFileSync(metaPath, "utf8"));
  } catch {
    console.error("sync-package-checker: no package-checker.meta.json — run `node scripts/sync-package-checker.mjs --ref <tag>` first");
    process.exit(1);
  }
  let ok = true;

  let shBuf;
  try {
    shBuf = readFileSync(shPath);
  } catch {
    console.error("sync-package-checker: DRIFT — src/vendor/package-checker.sh is missing");
    process.exit(1);
  }
  const shHash = sha256(shBuf);
  if (shHash !== meta.sha256["package-checker.sh"]) {
    console.error(`sync-package-checker: DRIFT in src/vendor/package-checker.sh — vendored bytes differ from the ${meta.tag} pin`);
    ok = false;
  }

  let tsActual;
  try {
    tsActual = readFileSync(tsPath, "utf8");
  } catch {
    console.error("sync-package-checker: DRIFT — src/vendor/package-checker-script.ts is missing");
    process.exit(1);
  }
  const tsHash = sha256(Buffer.from(tsActual, "utf8"));
  if (tsHash !== meta.sha256["package-checker-script.ts"]) {
    console.error(`sync-package-checker: DRIFT in src/vendor/package-checker-script.ts — vendored bytes differ from the ${meta.tag} pin`);
    ok = false;
  }

  // Re-render the .ts from the vendored .sh bytes and assert byte-identity —
  // catches a tampered .ts even when someone (or a re-run) recomputed a
  // consistent-looking hash by hand, and catches a tampered .sh whose hash
  // still happens to match a stale meta entry.
  const rendered = renderTs(meta.tag, shBuf);
  if (rendered !== tsActual) {
    console.error("sync-package-checker: DRIFT — src/vendor/package-checker-script.ts is not byte-identical to a fresh render of package-checker.sh");
    ok = false;
  }

  if (!ok) process.exit(1);
  console.log(`sync-package-checker: vendored package-checker.sh matches the ${meta.tag} pin (${meta.scriptVersion})`);
  process.exit(0);
}

const refIdx = args.indexOf("--ref");
const ref = refIdx !== -1 ? args[refIdx + 1] : undefined;
if (!ref) {
  console.error("usage: sync-package-checker.mjs --ref <tag>   |   sync-package-checker.mjs --check");
  process.exit(1);
}

mkdirSync(vendorDir, { recursive: true });

const url = `https://raw.githubusercontent.com/maxgfr/package-checker.sh/${ref}/script.sh`;
const res = await fetch(url);
if (!res.ok) {
  console.error(`sync-package-checker: ${url} -> HTTP ${res.status}`);
  process.exit(1);
}
const shBuf = Buffer.from(await res.arrayBuffer());
writeFileSync(shPath, shBuf);
console.log(`sync-package-checker: src/vendor/package-checker.sh (${shBuf.length} bytes)`);

// The script embeds its version greppably (`VERSION="1.11.4"`) — refuse a
// tag/content mismatch (mirrors sync-engine.mjs's ENGINE_VERSION check).
const shText = shBuf.toString("utf8");
const version = shText.match(/^VERSION="([^"]+)"/m)?.[1];
if (!version || `v${version}` !== ref) {
  console.error(`sync-package-checker: script says VERSION=${version ?? "?"} but the pinned ref is ${ref}`);
  process.exit(1);
}

const tsText = renderTs(ref, shBuf);
writeFileSync(tsPath, tsText);
console.log(`sync-package-checker: src/vendor/package-checker-script.ts (${tsText.length} bytes)`);

const sums = {
  "package-checker.sh": sha256(shBuf),
  "package-checker-script.ts": sha256(Buffer.from(tsText, "utf8")),
};

writeFileSync(metaPath, JSON.stringify({ tag: ref, scriptVersion: version, sha256: sums, syncedAt: new Date().toISOString() }, null, 2) + "\n");
console.log(`sync-package-checker: pinned package-checker.sh ${ref}`);
